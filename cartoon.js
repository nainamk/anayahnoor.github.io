// Get canvas
var canvas = document.getElementById("myCanvas");
var ctx = canvas.getContext("2d");

/////////////////////////////////////////////////////
// BACKGROUND
/////////////////////////////////////////////////////
ctx.fillStyle = "skyblue";
ctx.fillRect(0, 0, canvas.width, canvas.height);

/////////////////////////////////////////////////////
// SUN
/////////////////////////////////////////////////////
ctx.beginPath();
ctx.arc(700, 80, 50, 0, Math.PI * 2);
ctx.fillStyle = "yellow";
ctx.fill();
ctx.stroke();

/////////////////////////////////////////////////////
// GROUND
/////////////////////////////////////////////////////
ctx.fillStyle = "green";
ctx.fillRect(0, 350, canvas.width, 150);

/////////////////////////////////////////////////////
// HOUSE BASE
/////////////////////////////////////////////////////
ctx.fillStyle = "brown";
ctx.fillRect(250, 200, 300, 200);

/////////////////////////////////////////////////////
// ROOF
/////////////////////////////////////////////////////
ctx.beginPath();
ctx.moveTo(250, 200);
ctx.lineTo(400, 100);
ctx.lineTo(550, 200);
ctx.closePath();
ctx.fillStyle = "red";
ctx.fill();
ctx.stroke();

/////////////////////////////////////////////////////
// DOOR
/////////////////////////////////////////////////////
ctx.fillStyle = "black";
ctx.fillRect(370, 300, 60, 100);

/////////////////////////////////////////////////////
// WINDOWS
/////////////////////////////////////////////////////
ctx.fillStyle = "lightblue";
ctx.fillRect(280, 240, 50, 50);
ctx.fillRect(470, 240, 50, 50);

/////////////////////////////////////////////////////
// CAPTION TEXT
/////////////////////////////////////////////////////
ctx.fillStyle = "black";
ctx.font = "20px Arial";
ctx.fillText("Welcome to My Cartoon House!", 250, 30);

/////////////////////////////////////////////////////
// FOR LOOP + TRANSLATE (GRASS)
/////////////////////////////////////////////////////
for (let i = 0; i < 20; i++) {
    ctx.save();
    ctx.translate(i * 40, 400);

    ctx.beginPath();
    ctx.moveTo(10, 0);
    ctx.lineTo(15, -20);
    ctx.lineTo(20, 0);
    ctx.strokeStyle = "darkgreen";
    ctx.stroke();

    ctx.restore();
}
